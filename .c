#include <stdio.h>
int main()
{
char str[10001];
int N,k=0;
scanf("%s\n%d",str,&N);
char mat[N][N];
for(int i=0;i<N;i++)
{
    if(i%2==0)
    {
        for(int j=0;j<N;j++)
        {
            mat[j][i] = str[k++];
        }
    }
    else
    {
        for(int j=N-1;j>=0;j--)
        {
            mat[j][i] = str[k++];
        }
    }
}
for(int i=0;i<N;i++)
{
    for(int j=0;j<N;j++)
    {
        printf("%c ",mat[i][j]);
    }
    printf("\n");
}
}
