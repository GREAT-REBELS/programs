def isPrime(num):
    flg = 1
    for i in range(2, num // 2 + 1):
        if num % i == 0:
            flg = 0
            return 0
    if flg:
        return 1

Num = int(input())
res = 2
K = 3
Num -= 1
while Num:
    if isPrime(K):
        res *= K
        Num -= 1
    K += 2
print(res)
