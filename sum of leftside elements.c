#include <stdio.h>
int main()
{
int N,sum=0;
scanf("%d",&N);
int arr[N];
for(int i=0;i<N;i++)
{
    scanf("%d",&arr[i]);
}
for(int i=0;i<N;i++)
{
sum=0;
    {
    for(int j=i-1;j>=0;j--)
    {
        sum+=arr[j];
    }
    }
    printf("%d ",sum);
}
}
